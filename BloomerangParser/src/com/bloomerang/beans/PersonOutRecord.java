package com.bloomerang.beans;

public class PersonOutRecord implements BloomerangRecord{
	
	String firstName;
	String lastName;
	String title;
	String suffix;
	String yearOfBirth;
	String email;
	String emailDomain;
	String homePhone;
	String homePhoneExtn;
	String workPhone;
	String workPhoneExtn;
	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getSuffix() {
		return suffix;
	}
	public void setSuffix(String suffix) {
		this.suffix = suffix;
	}
	public String getYearOfBirth() {
		return yearOfBirth;
	}
	public void setYearOfBirth(String yearOfBirth) {
		this.yearOfBirth = yearOfBirth;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getEmailDomain() {
		return emailDomain;
	}
	public void setEmailDomain(String emailDomain) {
		this.emailDomain = emailDomain;
	}
	public String getHomePhone() {
		return homePhone;
	}
	public void setHomePhone(String homePhone) {
		this.homePhone = homePhone;
	}
	public String getHomePhoneExtn() {
		return homePhoneExtn;
	}
	public void setHomePhoneExtn(String homePhoneExtn) {
		this.homePhoneExtn = homePhoneExtn;
	}
	public String getWorkPhone() {
		return workPhone;
	}
	public void setWorkPhone(String workPhone) {
		this.workPhone = workPhone;
	}
	public String getWorkPhoneExtn() {
		return workPhoneExtn;
	}
	public void setWorkPhoneExtn(String workPhoneExtn) {
		this.workPhoneExtn = workPhoneExtn;
	}
	
	

}
